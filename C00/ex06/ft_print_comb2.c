#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a[5];
	char	x;
	char	y;

	x = 0;
	while (x <= 98)
	{
		y = x + 1;
		while (y <= 99)
		{
			a[0] = '0' + (x / 10);
			a[1] = '0' + (x % 10);
			a[2] = ' ';
			a[3] = '0' + (y / 10);
			a[4] = '0' + (y % 10);
			write(1, &a, 5);
			if (x != 98 || y != 99)
				write(1, ", ", 2);
			y++;
		}
		x++;
	}
}
